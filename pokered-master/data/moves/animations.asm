; each animation is a list of subanimations
; and/or special effects, terminated by -1

;\1: sound_id
;\2: special_effect_id or subanimation_id
; if \2 is a subanimation_id:
;\3: tileset_id
;\4: delay
battle_anim: MACRO
	IF _NARG == 4
		db (\3 << 6) | \4
		db \1 - 1
		db \2
	ELSE
		db \2
		db \1 - 1
	ENDC
ENDM

